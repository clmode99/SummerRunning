// ヘッダファイルの読み込み ================================================
#include <DxLib.h>




// 関数の定義 ==============================================================

//----------------------------------------------------------------------
//! @brief 角度の単位変換([度]→[ラジアン])
//!
//! @param[in] angle 角度[度]
//!
//! @return 角度[ラジアン]
//----------------------------------------------------------------------
double ToRadians(double angle)
{
	return angle * DX_PI / 180.0;
}



//----------------------------------------------------------------------
//! @brief 角度の単位変換([ラジアン]→[度])
//!
//! @param[in] angle 角度[ラジアン]
//!
//! @return 角度[度]
//----------------------------------------------------------------------
double ToDegrees(double angle)
{
	return angle * 180.0 / DX_PI;
}
